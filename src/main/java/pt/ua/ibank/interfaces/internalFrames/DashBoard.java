    private void popular(ArrayList<transacoes> ltransacoes){
        DefaultTableModel modelo = (DefaultTableModel) t_table.getModel();
        modelo.setNumRows(0);

        if (ltransacoes != null) {
            for (transacoes tr : ltransacoes) {
                modelo.addRow(new Object[]{
                    tr.getDescricao(),
                    tr.getValor()
                });
            }
        }
    }

